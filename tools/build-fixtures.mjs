/**
 * Generates the shared test fixtures:
 *
 *   ios/Fixtures/ghost-run.gpx   — a steady ~4.2 m/s effort (the ghost)
 *   ios/Fixtures/live-run.gpx    — a negative-split effort that starts behind
 *                                  and overtakes near the end (the racer)
 *   ios/GhostRaceKit/Tests/GhostRaceKitTests/Fixtures/crosscheck.json
 *                                — expected values that pin the Swift geo
 *                                  math to this JS implementation
 *
 * Run: node tools/build-fixtures.mjs
 * The GPX files load into Xcode's simulator location playback (Features >
 * Location > GPX file) to drive a full simulated race.
 */

import { writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import {
  cumulativeDistances,
  distanceM,
  interpolatedDistance,
  positionAtDistance,
  project,
} from "./geo.mjs";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

// A ~1.2 km out-and-around course along Golden Gate Park's JFK Drive.
export const COURSE = [
  { lat: 37.77135, lon: -122.45589 },
  { lat: 37.77218, lon: -122.45472 },
  { lat: 37.77258, lon: -122.4531 },
  { lat: 37.77228, lon: -122.45141 },
  { lat: 37.77163, lon: -122.45001 },
  { lat: 37.77099, lon: -122.44882 },
  { lat: 37.77015, lon: -122.44801 },
  { lat: 37.76918, lon: -122.44771 },
];

const cumulative = cumulativeDistances(COURSE);
const courseLength = cumulative[cumulative.length - 1];

/**
 * Build an effort as {t, d} samples every `sampleS` seconds given a speed
 * profile function speedAt(fractionOfCourse) in m/s.
 */
function buildEffort(speedAt, sampleS = 5) {
  const points = [{ t: 0, d: 0 }];
  let t = 0;
  let d = 0;
  while (d < courseLength) {
    const v = speedAt(d / courseLength);
    t += sampleS;
    d = Math.min(courseLength, d + v * sampleS);
    points.push({ t, d: round2(d) });
  }
  return { points, durationS: t };
}

// Ghost: steady 4.2 m/s with a slight fade at the end.
const ghost = buildEffort((f) => (f < 0.8 ? 4.2 : 4.0));
// Racer: starts at 3.9 m/s, surges to 4.8 m/s over the last 40% — passes the
// ghost in the final stretch. Good drama, good cue coverage.
const live = buildEffort((f) => (f < 0.6 ? 3.9 : 4.8));

if (live.durationS >= ghost.durationS) {
  throw new Error(
    `fixture invariant broken: live (${live.durationS}s) must beat ghost (${ghost.durationS}s)`
  );
}

// -- GPX ------------------------------------------------------------------

function toGpx(name, effort, startIso = "2026-01-01T09:00:00Z") {
  const start = Date.parse(startIso);
  const waypoints = effort.points
    .map((p) => {
      const c = positionAtDistance(COURSE, cumulative, p.d);
      const time = new Date(start + p.t * 1000).toISOString();
      return `  <wpt lat="${c.lat.toFixed(6)}" lon="${c.lon.toFixed(6)}">\n    <time>${time}</time>\n  </wpt>`;
    })
    .join("\n");
  return `<?xml version="1.0" encoding="UTF-8"?>\n<gpx version="1.1" creator="GhostRace fixtures">\n  <metadata><name>${name}</name></metadata>\n${waypoints}\n</gpx>\n`;
}

// -- crosscheck expected values --------------------------------------------

// Points slightly off-course at known fractions, to pin down projection.
const projectionCases = [0.1, 0.33, 0.5, 0.77, 0.95].map((f) => {
  const on = positionAtDistance(COURSE, cumulative, f * courseLength);
  // Nudge ~8m north-east so offsetM is non-trivial.
  const point = { lat: on.lat + 0.00005, lon: on.lon + 0.00005 };
  const expected = project(point, COURSE, cumulative);
  return {
    point,
    expected: {
      distanceAlongM: round2(expected.distanceAlongM),
      offsetM: round2(expected.offsetM),
    },
  };
});

const interpolationCases = [0, 3, 62.5, 117, ghost.durationS, ghost.durationS + 60].map((t) => ({
  t,
  expected: round2(interpolatedDistance(t, ghost.points)),
}));

// Replay the full race through the same delta math GhostEngine uses, and
// record the moment the racer takes the lead plus the final outcome. The
// Swift GhostEngineTests replay the identical inputs and must agree.
function simulateRace() {
  let leadTakenAtS = null;
  for (let t = 0; t <= live.durationS; t++) {
    const mine = interpolatedDistance(t, live.points);
    const ghostD = interpolatedDistance(t, ghost.points);
    if (leadTakenAtS === null && mine - ghostD > 3 /* leadHysteresisM */) {
      leadTakenAtS = t;
    }
  }
  return {
    winner: "live",
    liveDurationS: live.durationS,
    ghostDurationS: ghost.durationS,
    leadTakenAtS,
  };
}

const crosscheck = {
  comment:
    "Generated by tools/build-fixtures.mjs. Pins GhostRaceKit's Swift geo/race math to the JS twin in tools/geo.mjs. Regenerate with: node tools/build-fixtures.mjs",
  toleranceM: 0.5,
  course: { polyline: COURSE, cumulative: cumulative.map(round2), lengthM: round2(courseLength) },
  projectionCases,
  interpolationCases,
  ghostEffort: { durationS: ghost.durationS, points: ghost.points },
  liveEffort: { durationS: live.durationS, points: live.points },
  race: simulateRace(),
};

function round2(x) {
  return Math.round(x * 100) / 100;
}

// -- write ------------------------------------------------------------------

const isMain = process.argv[1] === fileURLToPath(import.meta.url);
if (isMain) {
  const fixturesDir = join(root, "ios", "Fixtures");
  const swiftFixturesDir = join(root, "ios", "GhostRaceKit", "Tests", "GhostRaceKitTests", "Fixtures");
  mkdirSync(fixturesDir, { recursive: true });
  mkdirSync(swiftFixturesDir, { recursive: true });
  writeFileSync(join(fixturesDir, "ghost-run.gpx"), toGpx("GhostRace ghost run", ghost));
  writeFileSync(join(fixturesDir, "live-run.gpx"), toGpx("GhostRace live run", live));
  writeFileSync(join(swiftFixturesDir, "crosscheck.json"), JSON.stringify(crosscheck, null, 2));
  console.log(
    `course ${round2(courseLength)}m | ghost ${ghost.durationS}s | live ${live.durationS}s | lead taken at ${crosscheck.race.leadTakenAtS}s`
  );
}

export { ghost, live, courseLength, cumulative, crosscheck };
